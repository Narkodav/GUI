        Graphics::Utility::ShaderModuleData m_shaderModuleData;

        bool m_initialized = false;

    public:

        ShaderCache() noexcept = default;
        ShaderCache(ShaderCache&&) noexcept = default;

        //moving to an initialized device is undefined behavior, destroy before moving
        ShaderCache& operator=(ShaderCache&& other) noexcept {
            if (this == &other)
                return *this;

            assert(!m_initialized && "ShaderCache::operator=() - ShaderCache already initialized");

            m_shaderModuleData = std::exchange(other.m_shaderModuleData, Graphics::Utility::ShaderModuleData());
            m_initialized = std::exchange(other.m_initialized, false);

            return *this;
        };

        ShaderCache(const ShaderCache&) noexcept = delete;
        ShaderCache& operator=(const ShaderCache&) noexcept = delete;

        ~ShaderCache() { assert(!m_initialized && "ShaderCache was not destroyed!"); };

        void create(const Graphics::DeviceFunctionTable& functions, Graphics::DeviceRef device, std::filesystem::path shaderPath) {
            m_shaderModuleData.shaderCodes.resize(2);
            m_shaderModuleData.shaderModules.resize(static_cast<size_t>(ShaderSpecialisation::Count));
            m_shaderModuleData.shaderModuleCreateInfos.resize(static_cast<size_t>(ShaderSpecialisation::Count));
            m_shaderModuleData.shaderStages.resize(static_cast<size_t>(ShaderSpecialisation::Count));

            m_shaderModuleData.shaderCodes[0].resize(s_quadVertSpv.size());
            std::copy(s_quadVertSpv.begin(), s_quadVertSpv.end(), m_shaderModuleData.shaderCodes[0].begin());
            m_shaderModuleData.shaderCodes[1].resize(s_quadFragSpv.size());
            std::copy(s_quadFragSpv.begin(), s_quadFragSpv.end(), m_shaderModuleData.shaderCodes[1].begin());

            for (size_t i = 0; i < static_cast<size_t>(ShaderSpecialisation::Count); ++i) {
                m_shaderModuleData.shaderModuleCreateInfos[i].setShaderCode(m_shaderModuleData.shaderCodes[i]);
                m_shaderModuleData.shaderModules[i].create(functions, device, m_shaderModuleData.shaderModuleCreateInfos[i]);
                m_shaderModuleData.shaderStages[i] = Graphics::ShaderModule::inferShaderStage(m_shaderModuleData.shaderCodes[i]);
            }
            m_initialized = true;
        }

        void destroy(const Graphics::DeviceFunctionTable& functions, Graphics::DeviceRef device) {
            if (!m_initialized)
                return;

            for (auto& shader : m_shaderModuleData.shaderModules)
                shader.destroy(functions, device);

            m_initialized = false;
        }

        const Graphics::Utility::ShaderModuleData& getShaderModuleData() const {
            return m_shaderModuleData;
        }
